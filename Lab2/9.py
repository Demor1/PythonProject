import math

x = float(input("Введіть число: "))

print(f"sin(x) = {math.sin(x):.3f}")
print(f"cos(x) = {math.cos(x):.3f}")
print(f"log(x) = {math.log(x):.3f}")
print(f"sqrt(x) = {math.sqrt(x):.3f}")
print(f"exp(x) = {math.exp(x):.3f}")
print(f"ceil(x) = {math.ceil(x)}")
print(f"floor(x) = {math.floor(x)}")
